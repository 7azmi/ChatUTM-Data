def clean_chat_history(chat_text):
    """Process chat history and extract meaningful Q&A conversations in Markdown format."""
    prompt = f"""
    Extract useful question-answer or summarise conversations from the following chat history and format them in Markdown.
    Ignore messages with missing or irrelevant information (e.g., deleted messages, media omissions, security updates, daily announcements).
    The useful data usually follows a question-answer format.

    Formatting rules:
    - If a conversation consists of a direct question-answer pair, format it concisely.
    - If multiple users provide varied opinions, structure it as an "Opinions & Responses" section, including usernames and timestamps.
    - For any opinion-based discussion, structure it with a clear "Inquiry" and "Opinions & Responses" breakdown.
    - Generate multiple markdown sections inside the file so that they can be split smoothly using Python, but do not add a global title like "# ChatUTM Conversations".

    Chat History:
    {chat_text}

    Output in Markdown format:
    """

    client = openai.OpenAI(api_key=OPENAI_API_KEY)
    response = client.chat.completions.create(
        model="gpt-4o",
        messages=[
            {"role": "system",
             "content": "You are an AI assistant designed to structure extracted chat data into well-formatted Markdown content for ChatUTM. Your goal is to create knowledge retrieval documents that improve accessibility and usability within ChatUTM. Ensure clarity, proper structuring, and effective topic separation."},
            {"role": "user", "content": prompt}
        ]
    )

    return response.choices[0].message.content.strip()


